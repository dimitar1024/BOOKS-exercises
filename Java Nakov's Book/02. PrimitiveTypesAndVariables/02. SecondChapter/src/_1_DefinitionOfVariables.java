
public class _1_DefinitionOfVariables {

	public static void main(String[] args) {
		byte one = -115;
		byte two = 97;
		int three = 52130;
		short four = 10000;
		long five = 4825932;
		
		System.out.println("byte ->" + one);
		System.out.println("byte ->" + two);
		System.out.println("int ->" + three);
		System.out.println("short ->" + four);
		System.out.println("long ->" + five);
	}

}
