
public class _2_FloatAndDouble {

	public static void main(String[] args) {
		double one = 34.567839023;
		float two = 12.345f;
		double three = 8923.1234857;
		float four = 3456.091f;
		
		System.out.println("double -> " + one);
		System.out.println("float -> " + two);
		System.out.println("double -> " + three);
		System.out.println("float -> " + four);
 }

}
